package variables;

public class UnderstandingVariables {
    public static void main(String[] args){
        /*
        DataType   Variable  Value
        int     age     41
        Variable declaration - allocating memory
        String name;
         */
        String name;

        name = "John";
        System.out.println(name);
        name = "Mike";
        System.out.println(name);

        //DECLARING MULTIPLE VARIABLES
        int age1, age2, age3; // declaring multiple variables
        double d1= 25, d2 = 10, d3 = 2.5; //declaring multiple variables and assigning VALUE
        boolean b1 = true, b2, b3 = false;



    }
}
