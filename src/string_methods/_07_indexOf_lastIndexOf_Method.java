package string_methods;

public class _07_indexOf_lastIndexOf_Method {
    public static void main(String[] args) {

        String company = "TechGlobal School.";

        int firstIndexOf = company.indexOf('l');
        int lastIndexOf = company.lastIndexOf('l');

        System.out.println(firstIndexOf);
        System.out.println(lastIndexOf);

        System.out.println("Filiz".length());
        System.out.println("Filiz".indexOf('i'));

    }
}
